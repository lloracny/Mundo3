#Tuplas são IMUTÁVEIS

lanche = ('Hamburguer', 'Suco', 'Pizza', 'Pudim')
print(lanche) #Inteiro
print(len(lanche)) #Mostra quantos elementos possui a variável
print(lanche[1]) #Mostrar 2º elemento
print(lanche[-2]) #De trás para frente
print(lanche[1:3]) #Mostrar do elemento 1 ao 2 (3 é desconsiderado)
print(lanche[:2]) #Mostra do início ao elemento 1 (2 é desconsiderado
print(lanche[2:]) #Mostra do elemento 1 ao final
